const BASE_NEW = 100;

// ----------------------------

const cpiArr = Object.values(cpiJson);
const [BASE_OLD] = cpiArr;

const newCpr = [];

const ratio = BASE_NEW / BASE_OLD;

for (const cpiValue of cpiArr) {
  newCpr.push(cpiValue * ratio);
}

const output = JSON.stringify(newCpr, null, 2);

console.log(output);

