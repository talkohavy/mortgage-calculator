/**
 * This scripts takes an array of CPI values, and change their base.
 *
 * ## HOW TO USE
 *
 * 1. Copy the values from the `src/data/cpiData.json` file.
 * 2. Change the value of BASE_NEW
 * 3. Run the script
 *
 * That's it.
 *
 * The old base is the first value in the array.
 */

